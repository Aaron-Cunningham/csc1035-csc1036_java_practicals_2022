
public class Circle {

    public static void main(String[] args) {
        System.out.println("Practical 2.4");
        //Sets the radius
        double radius = 7.5;
        //Works out the Area
        double area = Math.PI * radius * radius;
        //Works out Perimeter
        double perimeter = 2 * Math.PI * radius;
        //Prints Perimeter
        System.out.println(perimeter);
        //Prints Area
        System.out.println(area);
    }
}
